package br.edu.iff.model;

public class Funcionario extends Pessoa {
	
	private String usuario;
	private int chave;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public int getChave() {
		return chave;
	}
	public void setChave(int chave) {
		this.chave = chave;
	}
	

}
